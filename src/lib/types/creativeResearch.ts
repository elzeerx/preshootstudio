/**
 * Creative Research Data Types
 * 
 * These types define the structure of creative/ideation research data 
 * for non-factual content (creative, personal, opinion)
 */

export interface CreativeResearchData {
  contentAngles: string[];           // Different ways to approach the topic
  hooks: string[];                   // Attention-grabbing openings
  storyBeats: string[];              // Narrative structure points
  emotionalTriggers: string[];       // What makes it relatable
  trendingFormats: string[];         // Popular video formats for this niche
  audienceQuestions: string[];       // What viewers want to know
  inspirationReferences: string[];   // Similar successful content
  uniqueValueProp: string;           // What makes this stand out
}
